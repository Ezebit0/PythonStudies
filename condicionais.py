# Exemplo de "if"

idade = 18
print("Exemplo de comando if")
if idade >= 18: # Maior ou igual
    print("Voce é maior de idade")
elif idade >= 12:
    print("Você é um adolescente")
else: # Exemplo de "else"
    print("Voce não é maior de idade")

